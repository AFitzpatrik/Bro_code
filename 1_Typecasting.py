#Typecasting = the process of converting a variable from one data type to another
#  str(), int(), float(), bool()


name = "Francis"
age = 20
is_student = False
gpa = 3.5

name = bool(name)
age = str(age)
is_student = float(is_student)

print(name)
print(age)
print(is_student)
print(gpa)


#FALSE 0, TRUE 1